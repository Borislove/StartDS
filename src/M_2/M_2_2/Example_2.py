# Арифметические операции

a = 25
b = 3
print(a * b)  # Умножение
print(a ** b)  # Возведение в степень
print(a / b)  # Деление, результат - число с плавающей точкой
print(a // b)  # Вычисление целой части от деление одного числа на другое
print(a % b)  # Вычисление остатка от деления одного числа на другое
