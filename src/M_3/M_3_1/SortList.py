# сортировка списков
my_list = ['бета', 'альфа', 'дельта', 'гамма']
print('Первоначальный порядок:', my_list)
my_list.sort()
print('Отсортированный порядок:', my_list)

print('----------------------------------')
#  отсортировать на убывание reverse=True
my_list_1 = ['бета', 'альфа', 'дельта', 'гамма']
print('Первоначальный порядок:', my_list_1)
my_list_1.sort(reverse=True)
print('Отсортированный порядок:', my_list_1)

